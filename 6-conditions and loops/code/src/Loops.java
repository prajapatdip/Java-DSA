public class Loops {
    public static void main(String[] args) {
//        Q: write numbers from 1 to 5
//        for loop
        for (int i=1; i<= 5; i++){
            System.out.println(i);
        }

//        while loop
        int i=1;
        while (i<=5){
            System.out.println(i);
            i++;
        }

//        do-while loop
        int j=1;
        do {
            System.out.println(j);
            j++;
        }while (j<=5);
    }
}
